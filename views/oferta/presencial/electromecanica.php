                <div class="b_lateral">
                    <p class="itlcuerpo"><a href="">Visión:</a> </p>
                    <br>
                    <span class="nota">La carrera de Ingeniería Electromecánica, será una licenciatura de excelencia académica acreditada, basada en estándares de calidad reconocidos nivel nacional e internacional, y habráde contribuir con la formación de profesionales en el área de electromecánica para elevar los niveles de vida y bienestar de la población en general.</span><br>
                    <br> <br>

                    <p class="itlcuerpo"><a href="">Misión:</a> </p>
                    <br>
                    <span class="nota">Formar profesionales de excelencia académica con un alto sentido social y comprometidos en la aplicación de los programas de impulso estatal y nacional para el desarrollo productivo y social continuo de la comunidad, capaces de promover el aprovechamiento racional de los recursos naturales y humanos, y de participar con eficiencia y eficacia en la solución de los retos que plantea el desarrollo actual de las naciones.</span><br>

                    <br><br>
                    <p class="itlcuerpo"><a href="../Programas/Ing Electromecanica IEME - 2010 - 210/Perfil-Objetivo Ingenieria Electromecanica.pdf" target="_blank">Objetivo General</a></p>
                    <br>
                    <span class="nota">Formar profesionistas de excelencia en Ingeniería Electromecánica, con actitud emprendedora, liderazgo y capacidad de: analizar, diagnosticar, diseñar, seleccionar, instalar, administrar, mantener e innovar sistemas electromecánicos, en forma eficiente, segura y económica, considerando las normas y estándares nacionales e internacionales de forma sustentable con plena conciencia ética, humanística y social.</span> <br>

                    <br><br>
                    <p class="itlcuerpo"> <a href="../Programas/Ing Electromecanica IEME - 2010 - 210/Perfil-Objetivo Ingenieria Electromecanica.pdf" target="_blank">Perfil de Egreso</a></p>

                    <br>
                    <span class="nota">
                        1. Formula, gestiona y evalúa proyectos de ingeniería relacionados con sistemas y dispositivos en el área electromecánica, proponiendo soluciones con tecnologías de vanguardia, en el marco del
                        desarrollo sustentable.
                        <br>
                        2. Diseña e implementa sistemas y dispositivos electromecánicos, utilizando estrategias para el uso
                        eficiente de la energía en los sectores productivo y de servicios apegado a normas y acuerdos
                        nacionales e internacionales vigentes.
                        <br>
                        3. Diseña e implementa estrategias y programas para el control y/o automatización de los procesos
                        productivos y los dispositivos en los sistemas electromecánicos.
                        <br>
                        4. Proyectar, gestionar, implementar y controlar actividades de instalación y operación de los sistemas electromecánicos para hacer eficientes los procesos productivos.
                        <br>
                        5. Formula administra y supervisa programas de mantenimiento para la continuidad y optimización de
                        procesos productivos.
                        <br>
                        6. Genera y participa en proyectos de investigación para el desarrollo científico y tecnológico
                        contribuyendo al bienestar social.
                        <br>
                        7. Ejerce actitudes de liderazgo y de trabajo en equipo, para la toma de decisiones a partir de un
                        sentido ético y profesional.
                        <br>
                        8. Asume una actitud emprendedora en la creación e incubación de empresas, para el desarrollo
                        económico.
                        <br>
                        9. Aplica herramientas computacionales de acuerdo a las tecnologías de vanguardia, para el diseño,
                        simulación y operación de sistemas electromecánicos acordes a la demanda del sector industrial.
                        <br>
                        10. Utiliza el lenguaje oral y escrito con claridad y fluidez para interactuar en distintos contextos sociales.
                        <br>
                        11. Comprende un segundo idioma para comunicar ideas e interpretar documentos de distinta índole.

                    </span>
