                <div class="b_lateral">
                    <p class="itlcuerpo"><a href="../Programas/Arquitectura ARQU - 2010 - 204/Perfil-Objetivo Arquitectura.pdf" target="_blank">Objetivo General&nbsp;</a></p>
                    <br>
                    <span class="nota">Formar profesionistas líderes con excelencia académica y humanística, capaces de planear diseñar, gestionar y construir el hábitat humano con alto desempeño y vocación de servicio a la sociedad.</span> <br>
                    <br><br>
                    <p class="itlcuerpo"> <a href="../Programas/Arquitectura ARQU - 2010 - 204/Perfil-Objetivo Arquitectura.pdf" target="_blank">Perfil de egreso&nbsp;</a></p>

                    <br>
                    <span class="nota">
                        1. Diseña proyectos urbano-arquitectónicos de manera integral, para crear el hábitat humano,
                        respetando los marcos normativos del entorno y contexto, aplicando criterios de diseño universal.
                        <br>
                        2. Selecciona, aplica y evalúa tecnologías, materiales y sistema constructivos, para la ejecución de obras, considerando su ciclo de vida.
                        <br>
                        3. Planea, gestiona y opera desarrollos urbanos, con visión sostenible para elevar la calidad de vida.
                        <br>
                        4. Selecciona y diseña estructuras e instalaciones viables y sustentables, para el desarrollo de
                        proyectos urbanos arquitectónicos.
                        <br>
                        5. Administra el proceso constructivo de las obras urbano-arquitectónicas para la optimización de los recursos, con base al marco normativo vigente.
                        <br>
                        6. Asesora en la valoración y conservación del patrimonio, re-arquitectura, proyectos de inversión
                        inmobiliaria y legislación urbana para la toma de decisiones de los sectores público y privado.
                        <br>
                        7. Dirige organismos y grupos inter y multidisciplinarios para la integración de proyectos urbanoarquitectónicos
                        <br>
                        8. Actúa de manera ética, en su ejercicio profesional para el bienestar de la sociedad y su entorno.
                    </span>
