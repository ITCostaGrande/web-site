                <p class="itlcuerpo"><a href="../Programas/Ing en Sistemas Computacionales ISIC - 2010 - 224/Perfil-Objetivo Ingenieria en Sistemas Computacionales.pdf" target="_blank">Objetivo General&nbsp;</a></p>
                <br>
                <span class="nota">Formar profesionistas líderes con visión estratégica y amplio sentido ético; capaz de diseñar, desarrollar, implementar y administrar tecnología computacional para aportar soluciones innovadoras
                    en beneficio de la sociedad; en un contexto global, multidisciplinario y sostenible.</span> <br>
                <br><br>
                <p class="itlcuerpo"> <a href="../Programas/Ing en Sistemas Computacionales ISIC - 2010 - 224/Perfil-Objetivo Ingenieria en Sistemas Computacionales.pdf" target="_blank">Perfil de egreso&nbsp;</a></p>

                <br>
                <span class="nota">
                    1. Implementa aplicaciones computacionales para solucionar problemas de diversos contextos,
                    integrando diferentes tecnologías, plataformas o dispositivos.
                    <br>
                    2. Diseña, desarrolla y aplica modelos computacionales para solucionar problemas, mediante la
                    selección y uso de herramientas matemáticas.
                    <br>
                    3. Diseña e implementa interfaces para la automatización de sistemas de hardware y desarrollo del
                    software asociado.
                    <br>
                    4. Coordina y participa en equipos multidisciplinarios para la aplicación de soluciones innovadoras en diferentes contextos.
                    <br>
                    5. Diseña, implementa y administra bases de datos optimizando los recursos disponibles, conforme a
                    las normas vigentes de manejo y seguridad de la información.
                    <br>
                    6. Desarrolla y administra software para apoyar la productividad y competitividad de las
                    organizaciones cumpliendo con estándares de calidad.
                    <br>
                    7. Evalúa tecnologías de hardware para soportar aplicaciones de manera efectiva.
                    <br>
                    8. Detecta áreas de oportunidad empleando una visión empresarial para crear proyectos aplicando las
                    Tecnologías de la Información y Comunicación.
                    <br>
                    9. Diseña, configura y administra redes de computadoras para crear soluciones de conectividad en la
                    organización, aplicando las normas y estándares vigentes.</span>
