                <br><br>
                <p class="itlcuerpo"><a href="../Programas/Contador Publico COPU - 2010 - 205/Perfil-Objetivo Contador Publico.pdf" target="_blank">OBJETIVO GENERAL&nbsp;</a></p>

                <span class="nota">Formar profesionales competitivos, capaces de diseñar, establecer, aplicar, controlar y evaluar sistemas de información financiera, administrativa y fiscal, dentro del marco legal vigente para la toma de decisiones de las entidades económicas nacionales e internacionales, con una actitud ética, crítica, emprendedora y de liderazgo, a través de la investigación y el uso de la tecnología de la información y comunicaciones, fomentando el desarrollo sustentable.</span> <br>
                <br><br>
                <p class="itlcuerpo"> <a href="#">MISIÓN</a></p>

                <span class="nota">Formar personas integras en el área de la contaduría pública, competitivas nacional e internacionalmente en su campo profesional, que al mismo tiempo sea ciudadanos comprometidos con el desarrollo económico, político, social, cultural y sustentable con sentido de responsabilidad y ética consigo mismo y su entorno. </span> <br>
                <br><br>
                <p class="itlcuerpo"> <a href="#">VISIÓN</a> </p>
                <span class="nota"> Ser de los mejores institutos que ofrece la carrera de Contador Público reconocido a nivel nacional e internacional por su compromiso permanente con la excelencia en la formación de profesionales con valores solidarios e institucionales que contribuyen al desarrollo sustentable de su entorno.
                </span><br><br><br>
                <p class="itlcuerpo"><a href="#">PERFIL DE INGRESO&nbsp; </a></p>
                <span class="nota">
                    <ul>
                        <li>El aspirante de la carrera de Contador Público debe poseer las siguientes características:</li>
                        <li> Manejo de las tecnologías de la información y comunicación.</li>
                        <li> Habilidad para el trabajo en equipo. </li>
                        <li> Creatividad </li>
                        <li>Solucionador de retos.</li>
                    </ul>
                </span>
                <br><br>
                <p class="itlcuerpo"> <a href="../Programas/Contador Publico COPU - 2010 - 205/Perfil-Objetivo Contador Publico.pdf" target="_blank">PERFIL DE EGRESO&nbsp;</a></p>

                <span class="nota">
                    1. Diseña, implanta, controla, evalúa, asesorara e innova sistemas de información financiera,
                    administrativa, fiscal y de auditoría en entidades económicas para la generación, análisis de
                    información financiera con apego a las Normas de Información Financiera, nacionales e
                    internacionales y la toma de decisiones.
                    <br>
                    2. Audita sistemas financieros, fiscales y administrativos de las entidades económicas con apego a las Normas Internacionales de Auditoria vigentes para la emisión de un dictamen.
                    <br>
                    3. Aplica el marco legal y pertinente a las características y necesidades de la entidad económica
                    dentro del campo profesional para el cumplimiento de las disposiciones vigentes.
                    <br>
                    4. Administra estratégicamente los recursos de las entidades económicas para el cumplimiento de sus
                    objetivos.
                    <br>
                    5. Aplica en el ejercicio profesional el código de ética del contador público vigente para enaltecer a la profesión.
                    <br>
                    6. Utiliza las tecnologías de información y comunicación para eficientar los procesos y la toma de
                    decisiones.
                    <br>
                    7. Desarrolla investigación asumiendo una actitud de liderazgo, compromiso y servicio con su entorno social, para su difusión.
                    <br>
                    8. Elabora y evalúa proyectos de inversión de acuerdo a las características y necesidades del entorno para la generación de entidades económicas con actitud de liderazgo y compromiso con el medio ambiente.
                    <br>
                    9. Trabaja en equipos multidisciplinarios para el logro de los resultados de las entidades con un
                    sentido de responsabilidad social y visión integradora.
                    <br>
                    10. Propone estrategias de mercadotecnia que permitan alcanzar los objetivos de las entidades
                    económicas.
                    <br>
                    11. Aplica métodos de análisis de información financiera para determinar las mejores alternativas de inversión y financiamiento.
                </span>
