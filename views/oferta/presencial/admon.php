            <div class="descripcion_carrera">
                <div class="b_lateral">
                    <p class="itlcuerpo"><a href="../Programas/Lic en Administracion LADM - 2010 -234/Perfil-Objetivo Licenciatura en Administracion.pdf" target="_blank">Objetivo General</a></p>
                    <br>
                    <span class="nota">Formar profesionales de la administración comprometidos con las demandas y oportunidades del entorno, con una visión estratégica, humanista y global, que actúen como agentes de cambio, a través del diseño, innovación y dirección en organizaciones.</span>
                    <br><br>
                    <p class="itlcuerpo"> <a href="#" target="_blank">MISIÓN </a></p>
                    <span class="nota"> Formar profesionales de la administración, capaces de tomar decisiones, asumir riesgos, desarrollar innovaciones, interpretar tendencias económicas, sociales, políticas, culturales, ambientales y tecnológicas, generando su inserción competitiva en el entorno global con sustentabilidad ética y responsabilidad social.</span>
                    <br><br>
                    <p class="itlcuerpo"> <a href="#" target="_blank"> VISIÓN </a></p>

                    <span class="nota">El programa de Licenciatura en Administración se constituirá en el largo plazo como un sistema académico certificado y consolidado en la Costa Grande de Guerrero, caracterizado por contar con diseño curricular flexible, basado en el modelo educativo vigente.</span>
                    <br><br>
                    <p class="itlcuerpo"> <a href="">PERFIL DE INGRESO</a> </p>
                    <span class="nota"> Deberá contar con los conocimientos mínimos de las áreas de humanidades, ciencias sociales y naturales, además de demostrar su capacidad de razonamiento verbal y matemático para ser capaz de interpretar lecturas, entender y utilizar analogías y demostrar la amplitud de su vocabulario. Debe ser una persona con independencia de criterio, capaz de formarse un juicio propio, a partir de un análisis de datos, así como de razonar de manera eficiente bajo presión de tiempo.
                    </span>
                    <br><br>
                    <p class="itlcuerpo"> <a href="../Programas/Lic en Administracion LADM - 2010 -234/Perfil-Objetivo Licenciatura en Administracion.pdf" target="_blank">Perfil de Egreso</a></p>

                    <br>
                    <span class="nota">
                        1. Integra los procesos gerenciales, de administración, de innovación y las estrategias para alcanzar la productividad y competitividad de las organizaciones.
                        <br>
                        2. Adapta las etapas de los procesos a las nuevas tendencias y enfoques de la administración, para la optimización de los recursos y el manejo de los cambios organizacionales, de acuerdo a las
                        necesidades del entorno.
                        <br>
                        3. Desarrolla habilidades directivas basadas en la ética y la responsabilidad social, que le permitan integrar y coordinar equipos interdisciplinarios y multidisciplinarios para el crecimiento de la organización.
                        <br>
                        4. Crea y desarrolla proyectos sustentables aplicando métodos de investigación de vanguardia, con un enfoque estratégico, multicultural y humanista.
                        <br>
                        5. Dirige la organización hacia la consecución de sus objetivos, a través de la coordinación de
                        esfuerzos y el desarrollo creativo.
                        <br>
                        6. Diseña organizaciones que contribuyan a la transformación económica y social identificando las
                        oportunidades de negocios.
                        <br>
                        7. Interpreta y aplica el marco legal vigente nacional e internacional, para dar certeza jurídica a las organizaciones.
                        <br>
                        8. Interpreta información financiera y económica para la toma de decisiones en las organizaciones.
                        <br>
                        9. Desarrolla el capital humano a través de la utilización de técnicas y herramientas gerenciales para la toma de decisiones.
                        <br>
                        10. Implementa y administra sistemas de gestión de calidad para orientarlos a la mejora continua, con la finalidad de lograr la eficacia y eficiencia de la organización.
                        <br>
                        11. Aplica las tecnologías de la información y comunicación en el diseño de estrategias que optimicen el trabajo y desarrollo de las organizaciones.
                        <br>
                        12. Posee una visión multidisciplinaria para generar acciones ante escenarios de contingencia
                        salvaguardando la integridad humana.
                        <br>
                        13. Diseña estrategias de mercadotecnia basadas en el análisis de la información interna y del entorno global, para asegurar el éxito de la comercialización de bienes y servicios de las organizaciones.
                    </span>
                    <br><br>
                    <p class="itlcuerpo"><a href="">CAMPO DE TRABAJO:</a> </p>
                    <span class="nota">
                        • Empresas públicas: Municipales, estatales y federales. <br>
                        • Empresas privadas: Industriales, comerciales y servicios. <br>
                        • Organizaciones de la sociedad civil. <br>
                        • Generadoras de su propia empresa <br>
                    </span>
                    <br><br>
                    <p class="itlcuerpo"> <a href="">ÁMBITO</a> </p>

                    <span class="nota"> Administración, Producción, Finanzas, Recursos Humanos, Mercadotecnia, Calidad.</span>
                    <br><br>
                    <p class="itlcuerpo"><a href="">POLÍTICAS</a> </p>
                    <span class="nota">
                        • Los licenciados en administración deberán: <br>
                        • Tener una participación activa, crítica propositiva. <br>
                        • Desarrollar actitud de servicio con la comunidad. <br>
                        • Aplicar aptitudes y habilidades de organización. <br>
                        • Establecer destreza para intervenir con enfoque interdisciplinario. <br>
                        • Generar habilidad de comunicación. <br>
                        • Ejercer liderazgo en su ámbito de aplicación laboral <br>
                        • Constituirse como gestores de cambio en el desarrollo económico local, regional, nacional e internacional. <br>
                        • Aplicar conocimientos, habilidades y actitudes proactivas para la toma de decisiones. <br>
                    </span>
