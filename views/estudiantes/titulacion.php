    <p class=MsoNormal><b>Documentos para trámite de título y cédula profesional:<o:p></o:p></b></p>

    <p class=MsoNormal>Para integrar su expediente debe presentar documentos
        originales y en algunos casos copias fotostáticas, se recomienda asistan a un
        centro de copiado de prestigio. Debe atender las siguientes recomendaciones
        para las copias fotostáticas o de lo contrario no se aceptarán.</p>

    <ol style='margin-top:0cm' start=1 type=1>
        <li class=MsoNormal style='mso-list:l0 level1 lfo1;tab-stops:list 36.0pt'>Todas
            las copias fotostáticas deben ser totalmente legibles, esto es, sin
            secciones borrosas, sin manchas, sin partes cortadas, no muy tenues ni muy
            obscuras.</li>

        <li class=MsoNormal style='mso-list:l0 level1 lfo1;tab-stops:list 36.0pt'>Si
            el documento original cuenta con información en el reverso deberá
            fotocopiar el reverso también.</li>
        <li class=MsoNormal style='mso-list:l0 level1 lfo1;tab-stops:list 36.0pt'>El
            número de copias por documento será de tres tantos.</li>
    </ol>

    <p class=MsoNormal><b>Los documentos originales son únicamente para cotejo de
            las copias fotostáticas, estos les serán devueltos de inmediato después de la
            revisión por el Departamento de Servicios Escolares.</b></p>

