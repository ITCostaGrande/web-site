        <p align="left"><strong>SUPERACIÓN:</strong> buscar constantemente ser mejores en el saber y en el hacer.<br>
            <strong>ESPÍRITU DE SERVICIO:</strong> satisfacer con calidad las necesidades y expectativas de nuestra comunidad.<br>
            <strong>TOLERANCIA:</strong> respetar las ideas y creencias de los demás para asegurar la convivencia armónica.<br>
            <strong>NACIONALISMO:</strong> preservar y respetar los valores culturales, cívicos, históricas y sociales del país.<br>
            <strong>ÉTICA:</strong> actuar siempre con honestidad, integridad, justicia y equidad.<br>
            <strong>LEALTAD:</strong> fortalecerla convicción de pertenencia y defender los principios institucionales.<br>
            <strong>CALIDAD:</strong> otorgar el servicio con base en las expectativas de satisfacción plena de la sociedad.<br>
            <strong>COMPROMISO:</strong> hacer nuestras las metas de la organización y estar siempre dispuestos al esfuerzo adicional.<br>
            <strong>ALTO DESEMPEÑO:</strong> propiciar la superación de los estándares de calidad, mejorando el resultado del trabajo a partir de los valores y la convicción de las personas.<br>
            <strong>RESPONSBAILIDAD:</strong> cumplir efectiva y oportunamente los compromisos asumidos.<br>
            <strong>TRABAJO EN EQUIPO:</strong> multiplicar acciones y actitudes que procuren beneficios comunes y propicien el desarrollo de las personas.
